def beat_method(results, method_name, verbal=False):
    #print(results)
    beat_cnt = 0
    total_cnt = 0
    advantage = {
        "MSE": 0,
        "SSIM": 0,
        "LPIPS": 0
    }
    for sequence in metric_references[method_name].keys():
        for metric in ["MSE", "SSIM", "LPIPS"]:
            ref_val = metric_references[method_name][sequence][metric]
            result_val = results.get(f"{sequence}/{metric}", None)
            if result_val is None:
                continue
            total_cnt += 1
            if metric == "SSIM":
                # Larger is better
                adv = result_val - ref_val
            else:
                # Smaller is better
                adv = ref_val - result_val
            advantage[metric] += adv
            if adv > 0:
                beat_cnt += 1
    print(f"Comparing results with {method_name}: Beated {beat_cnt} / {total_cnt}")
    print(f"Advantage (larger is better): ", advantage)
    print(f"Total advantage: ", sum(advantage.values()))
    return beat_cnt